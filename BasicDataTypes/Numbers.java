public class Numbers {
  public static void main(String[] args) {
    // 1
    int x;
    int y;

    x = 4;
    y = 3;

    System.out.println(x + y);
    System.out.println(x - y);
    System.out.println(x * y);
    System.out.println(x / y);

    // 2
    int x1 = 4;
    float y1 = 2.0f;

    System.out.println(x1 + y1);

    // 3
    double x2 = 4.2;
    double y2 = 3.2;
    float z = (float)(x2 / y2);

    System.out.println(z);
  }
}
